# Python e constructor function dara Casting kora hoy.Constructor function gulo holo: int(), float(), str().
#int()->er moddhe float value dei r string value dei sob gulai integer hisabei show korbe.
#float()->er moddhe int ba string je value ei sob gulai float hisabe dekhabe.
#str()->er moddhe int ba float je value ei sob gulai string hisabe dekhabe.

#Integers:
x= int(1)     #integer value
y= int(2.8)   #float value
z= int('3')   #string value
print(x)
print(y)
print(z)

#Floats:
x=float(1)          #integer value
y=float(2.8)        #float value
z=float("3")        #string value
w=float('4.2')      #string value
print(x)
print(y)
print(z)
print(w)

#Strings:
x=str('s1')     #string value
y=str(2)        #integer value
z=str(3.0)      #float value
print(x)
print(y)
print(z)