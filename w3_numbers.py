# There are 3 types of numeric / number
# 1. int
# 2. float
# 3. complex

# int number
x=1
y=34656464646
z=-100
print(type(x))
print(type(y))
print(type(z))

# float number
x=1.10
y=1.0
z=-35.59
print(type(x))
